float ZEAngle::FastSin(float Value)
{
	ZEUInt Quadrant;
	ZEInt Sign = 1.0f;

	float* Sin0 = &SinTable[0];
	float* Sin90 = &SinTable[900];

	float Degrees = Value * (180.0f / ZE_PI);
	ZEUInt Index = 0;
	
	ZEInt IntDegrees = Degrees;
	float Fraction = Degrees - (float)IntDegrees;

	IntDegrees = IntDegrees % 360;

	if(IntDegrees < 0)	
		Quadrant = 3 - (IntDegrees / 90);
	else				
		Quadrant = IntDegrees / 90;

	float Value1, Value2;

	switch(Quadrant)
	{
		case 0:
			Index = 0 + IntDegrees * 10 + (ZEUInt)(Fraction * 10.0f + 0.5f);
			break;

		case 1:
			IntDegrees = IntDegrees - 90;
			Index = 900 - IntDegrees * 10 - (ZEUInt)(Fraction * 10.0f + 0.5f);
			break;

		case 2:
			Sign = -1;
			IntDegrees = IntDegrees - 180;
			Index = 0 + IntDegrees * 10 + (ZEUInt)(Fraction * 10.0f + 0.5f);
			break;

		case 3:
			Sign = -1;
			IntDegrees = IntDegrees - 270;
			Index = 900 - IntDegrees * 10 - (ZEUInt)(Fraction * 10.0f + 0.5f);
			break;
	}

	return Sign * ZEMath::Lerp(Sin0[Index], Sin0[Index + 1], Fraction);
}

float ZEAngle::FastCos(float Value)
{
	return ZEAngle::FastSin(ZE_PI_2 - Value);
}

float ZEAngle::Range(float Angle)
{
	float ModAngle = ZEMath::Mod(Angle, ZE_PIx2);
	if(ModAngle > ZE_PI)
		return ModAngle - ZE_PIx2;
	else if(ModAngle < -ZE_PI)
		return ModAngle + ZE_PIx2;

	return ModAngle;
}

float ZEAngle::Distance(float Angle1, float Angle2)
{
	return Range(ZEMath::Abs(Angle1 - Angle2));
}

